def generate_passphrase(word_count: int = 6) -> str:
    """Generate a passphrase of random lowercase words joined by dashes.

    Uses cryptographically secure random selection. With 2048 words and
    6 words, entropy is 6 * log2(2048) = 66 bits.

    Args:
        word_count: Number of words in the passphrase (default: 6)

    Returns:
        Passphrase string like "velvet-copper-bridge-cabin-lamp-frost"
    """
    words = [secrets.choice(WORDLIST) for _ in range(word_count)]
    return "-".join(words)
